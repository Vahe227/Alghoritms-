// How I Solve This Problem

// To solve this problem, we first create a variable i that will be used as the loop index. We then enter a loop to iterate through the entire nums array.
// Inside the loop, we check if the current element nums[i] is equal to the value val.
// If it is equal, we remove that element from the array using a removal operation (such as splice).
// Otherwise, we simply increment i by 1 to move to the next element.
// By handling the loop this way, we make sure that every occurrence of val is removed from the array.
// Finally, we return the modified nums array as the result.
// The time complexity of this solution is O(N), because the algorithm iterates through the input array once, making it efficient for this type of problem.

/**
 * @param {number[]} nums
 * @param {number} val
 * @return {number}
 */
var removeElement = function(nums, val) {
    let i = 0;
    while(i < nums.length) {
        if(nums[i] == val) {
            nums.splice(i,1);
        } else {
            i++;
        };
    }
    return nums.length;
};